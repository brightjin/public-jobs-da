USE dive_recruit;

-- 테이블: 채용공고
-- 원본 크기: 295행 x 16열
CREATE TABLE IF NOT EXISTS `채용공고` (
    id INT AUTO_INCREMENT PRIMARY KEY,
    `기관명` VARCHAR(12) NOT NULL DEFAULT '',
    `공고명` VARCHAR(255) NOT NULL DEFAULT '',
    `공고시작일` DATE NOT NULL ,
    `공고마감일` DATE NOT NULL ,
    `접수시작일` DATE NOT NULL ,
    `접수마감일` DATE NOT NULL ,
    `접수방법` VARCHAR(34) NOT NULL DEFAULT '',
    `접수대행` VARCHAR(255) NOT NULL DEFAULT '',
    `일반전형` VARCHAR(255) NOT NULL DEFAULT '',
    `채용인원` VARCHAR(6) NOT NULL DEFAULT '',
    `채용방법` VARCHAR(30) NOT NULL DEFAULT '',
    `전형방법` VARCHAR(86) NOT NULL DEFAULT '',
    `임용시기` VARCHAR(28) NOT NULL DEFAULT '',
    `임용조건` TEXT NOT NULL DEFAULT '',
    `담당부서` VARCHAR(22) NOT NULL DEFAULT '',
    `연락처` VARCHAR(24) NOT NULL DEFAULT '',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

